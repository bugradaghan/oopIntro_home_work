package oopIntroHomework;

public class Course {
	int id;
	String courseName;
	String teacher;
	int completionAmount;
	
	public Course() {};
	
	public Course(int id, String courseName, String teacher, int completionAmount ) {
		
		this.id=id;
		this.courseName=courseName;
		this.teacher=teacher;
		this.completionAmount=completionAmount;
		
		
	}
	


}
